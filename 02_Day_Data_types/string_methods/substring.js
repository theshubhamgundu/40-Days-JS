// substring(): It takes two arguments,the starting index and the stopping index but it doesn't include the stopping index.
let string = 'JavaScript'
console.log(string.substring(0,4))   // Java
console.log(string.substring(4,10))     // Script
console.log(string.substring(4))    // Script
let country = 'Karnataka'
console.log(country.substring(0, 3))   // Kar
console.log(country.substring(3, 8))   // natak
console.log(country.substring(3))   // nataka